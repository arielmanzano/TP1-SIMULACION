package numero_generado;

/**
 *
 * @author Usuario
 */
public class NumeroGenerado {
    private int n;
    private double valor;

    public NumeroGenerado(int n, double valor) {
        this.n = n;
        this.valor = valor;
    }

    public int getN() {
        return n;
    }

    public double getValor() {
        return valor;
    }

    public void setN(int n) {
        this.n = n;
    }

    public void setValor(double valor) {
        this.valor = valor;
    }
    
    
}
